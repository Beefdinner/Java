
public class ShortcutOperators {

	public static void main(String[] args) 
	{
		int a = 6;
		int b = 2;
		
		System.out.println("At the begining , a is " + a);
		System.out.println("Increment a with a prefix notation:  " + ++a);
		System.out.println("In the end A is " + a);
		
		System.out.println("\nAt the begining , b is " + b);
		System.out.println("Increment with a postfix notation now b is " + b++);
		System.out.println("In the end b is " + b);
		
	}

}
